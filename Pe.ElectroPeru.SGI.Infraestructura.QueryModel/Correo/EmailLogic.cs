using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Pe.ElectroPeru.SGI.Infraestructura.QueryModel.Correo
{
    public class EmailLogic
    {
        /// <summary>
        /// EmailLogic
        /// </summary>
        public EmailLogic()
        {
            Para = new List<DireccionesLogic>();
            ConCopia = new List<DireccionesLogic>();
            Adjuntos = new List<AdjuntosLogic>();
        }
        /// <summary>
        /// Para
        /// </summary>
        public List<DireccionesLogic> Para { get; set; }
        /// <summary>
        /// ConCopia
        /// </summary>
        public List<DireccionesLogic> ConCopia { get; set; }
        /// <summary>
        /// Asunto
        /// </summary>
        public string Asunto { get; set; }
        /// <summary>
        /// Mensaje
        /// </summary>
        public string Mensaje { get; set; }
        /// <summary>
        /// Adjuntos
        /// </summary>
        public List<AdjuntosLogic> Adjuntos { get; set; }
    }
}
