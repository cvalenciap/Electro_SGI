        /// Estado de Registro
        /// </summary>
        public string EstadoRegistro { get; set; }
        /// <summary>
        /// Usuario
        /// </summary>
        public string Usuario { get; set; }
        /// <summary>
        /// Terminal
        /// </summary>
        public string Terminal { get; set; }
    }

}
