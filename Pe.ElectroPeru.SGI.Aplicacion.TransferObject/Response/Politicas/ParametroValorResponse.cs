        /// Código de Parámetro
        /// </summary>
        public int CodigoParametro { get; set; }
        /// <summary>
        /// Código Identificador
        /// </summary>
        public string CodigoIdentificador { get; set; }
        /// <summary>
        /// Código de Valor
        /// </summary>
        public int CodigoValor { get; set; }
